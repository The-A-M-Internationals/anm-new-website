async function updateBlog() {
    try {
        const q = query(collection(db, "articles"), where("featureArticle", "in", [true, "true", "Yes"]));
        const snap = await getDocs(q);
        
        if (!snap.empty) {
            const docRef = doc(db, "articles", snap.docs[0].id);
            await updateDoc(docRef, arabicContent);
            console.log("Featured article updated with Arabic translation!");
        } else {
            console.log("No featured article found to update.");
        }
        process.exit(0);
    } catch(e) {
        console.error(e);
        process.exit(1);
    }
}

